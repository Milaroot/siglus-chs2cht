pub fn decrypt1(data: &[u8], key: &[u8; 16]) -> Vec<u8> {
    data.iter()
        .enumerate()
        .map(|(i, &b)| b ^ key[i & 15])
        .collect()
}

pub fn decrypt2(data: &[u8], key_type: u8) -> Vec<u8> {
    let table = if key_type == 0 { &KEY_TABLE_0 } else { &KEY_TABLE_1 };
    data.iter()
        .enumerate()
        .map(|(i, &b)| b ^ table[i & 255])
        .collect()
}

pub fn decrypt4(data: &[u8]) -> Vec<u8> {
    decrypt2(data, 1)
}

pub fn decrypt_string(data: &[u8], length: usize, index: usize) -> Vec<u8> {
    let local_key = ((28807u32 * index as u32) % 65536) as u16;
    let mut result = vec![0u8; length * 2];
    for n in 0..length {
        let char_val = u16::from_le_bytes([data[n * 2], data[n * 2 + 1]]);
        let xored = local_key ^ char_val;
        result[n * 2..n * 2 + 2].copy_from_slice(&xored.to_le_bytes());
    }
    result
}

pub fn decompress(inbuf: &[u8], out_size: usize) -> Vec<u8> {
    let mut outbuf = vec![0u8; out_size];
    let mut in_pos = 0;
    let mut out_pos = 0;
    while out_pos < out_size {
        let flag = inbuf[in_pos];
        in_pos += 1;
        let mut f = flag;
        for _ in 0..8 {
            if out_pos >= out_size {
                break;
            }
            if f & 1 != 0 {
                outbuf[out_pos] = inbuf[in_pos];
                in_pos += 1;
                out_pos += 1;
            } else {
                let lo = inbuf[in_pos] as u16;
                let hi = inbuf[in_pos + 1] as u16;
                in_pos += 2;
                let reference = lo | (hi << 8);
                let copy_len = (reference & 0x0F) as usize + 2;
                let offset = (reference >> 4) as usize;
                for _ in 0..copy_len {
                    outbuf[out_pos] = outbuf[out_pos - offset];
                    out_pos += 1;
                }
            }
            f >>= 1;
        }
    }
    outbuf
}

pub fn compress(data: &[u8]) -> Vec<u8> {
    let src_len = data.len();
    if src_len == 0 {
        let mut out = Vec::with_capacity(8);
        out.extend_from_slice(&8u32.to_le_bytes());
        out.extend_from_slice(&0u32.to_le_bytes());
        return out;
    }

    const HASH_SIZE: usize = 1 << 15;
    const HASH_MASK: usize = HASH_SIZE - 1;
    const MAX_OFF: usize = 4095;
    const MAX_LEN: usize = 17;
    const MAX_CHAIN: usize = 32;

    let mut head = vec![-1i32; HASH_SIZE];
    let mut prev = vec![-1i32; src_len];

    let hash3 = |p: usize| -> usize {
        if p + 2 >= src_len {
            return 0;
        }
        ((data[p] as usize) << 10 ^ (data[p + 1] as usize) << 5 ^ data[p + 2] as usize)
            & HASH_MASK
    };

    let mut result = Vec::with_capacity(src_len);
    let mut pos = 0;

    while pos < src_len {
        let flag_pos = result.len();
        result.push(0);
        let mut flag = 0u8;

        for bit in 0..8 {
            if pos >= src_len {
                break;
            }

            let mut best_len = 1usize;
            let mut best_off = 0usize;

            if pos + 1 < src_len {
                let h = hash3(pos);
                let mut chain_pos = head[h];
                let mut chain_count = 0;
                let limit = if pos > MAX_OFF { pos - MAX_OFF } else { 0 };
                let remain = std::cmp::min(src_len - pos, MAX_LEN);

                while chain_pos >= limit as i32 && chain_count < MAX_CHAIN {
                    let cp = chain_pos as usize;
                    let off = pos - cp;
                    if off < 1 {
                        break;
                    }
                    let mut ml = 0;
                    while ml < remain && data[cp + ml] == data[pos + ml] {
                        ml += 1;
                    }
                    if ml > best_len {
                        best_len = ml;
                        best_off = off;
                        if best_len >= remain {
                            break;
                        }
                    }
                    chain_pos = prev[cp];
                    chain_count += 1;
                }
            }

            if best_len >= 2 && best_off >= 1 {
                for j in 0..best_len {
                    let hp = hash3(pos + j);
                    prev[pos + j] = head[hp];
                    head[hp] = (pos + j) as i32;
                }
                let reference = ((best_off << 4) | (best_len - 2)) as u16;
                result.push(reference as u8);
                result.push((reference >> 8) as u8);
                pos += best_len;
            } else {
                let hp = hash3(pos);
                prev[pos] = head[hp];
                head[hp] = pos as i32;
                flag |= 1 << bit;
                result.push(data[pos]);
                pos += 1;
            }
        }

        result[flag_pos] = flag;
    }

    let comp_size = (result.len() + 8) as u32;
    let mut out = Vec::with_capacity(result.len() + 8);
    out.extend_from_slice(&comp_size.to_le_bytes());
    out.extend_from_slice(&(src_len as u32).to_le_bytes());
    out.extend_from_slice(&result);
    out
}
